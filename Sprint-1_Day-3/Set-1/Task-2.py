# 2. **Data Type Play**: Create variables of each data type (integer, float, string, boolean, list, tuple, dictionary, set) and print their types and values.
#     - *Input*: None
#     - *Output*: "Type of variable1: <class 'int'>, value: 10..."


# Integer
integer = 10
print(f"Type of integer: {type(integer)}, value: {integer}")

# Float
float = 3.14
print(f"Type of float: {type(float)}, value: {float}")

# String
string = "Hello, world!"
print(f"Type of string: {type(string)}, value: {string}")

# Boolean
boolean = True
print(f"Type of boolean: {type(boolean)}, value: {boolean}")

# List
list = [1, 2, 3, 4, 5]
print(f"Type of list: {type(list)}, value: {list}")

# Tuple
tuple = (10, 20, 30)
print(f"Type of tuple: {type(tuple)}, value: {tuple}")

# Dictionary
dict = {"name": "John", "age": 30, "city": "New York"}
print(f"Type of dict: {type(dict)}, value: {dict}")

# Set
set = {1, 2, 3, 3, 4, 5, 5}
print(f"Type of set: {type(set)}, value: {set}")
